import java.util.*;
class Spring
{
	public static void main(String args[])
	{
		Scanner sc=new Scanner(System.in);
		int march=sc.nextInt();
		int june=sc.nextInt();
		if (march>=20 && june<=20)
		{
			System.out.println("Its spring season:");
		}
		else 
		{
			System.out.println("Its not a spring season:");
		}
	}
}
			